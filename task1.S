.section .data
    buffer: .fill 256, 1, 0  // 256-байтний буфер, ініціалізований нулями

.section .text
    .global _start

_start:
    // Виклик getcwd (номер 17)
    ldr x0, =buffer  // Адреса буфера
    mov x1, #256     // Розмір буфера
    mov x8, #17      // Номер системного виклику getcwd
    svc #0           // Виклик системної функції

    // Виклик write (номер 64) для виведення каталогу
    mov x0, #1       // Файловий дескриптор stdout
    ldr x1, =buffer  // Адреса буфера з каталогом
    mov x2, #256     // Кількість байт для виведення
    mov x8, #64      // Номер системного виклику write
    svc #0           // Виклик системної функції

    // Зміна прав доступу на файл (chmod, номер 52)
    ldr x0, =filename  // Адреса імені файлу
    mov x1, #0777      // Права доступу (rwx для всіх)
    mov x8, #52        // Номер системного виклику chmod
    svc #0             // Виклик системної функції

    // Повернення коду 1
    mov x0, #1       // Код повернення 1
    mov x8, #93      // Номер системного виклику exit
    svc #0           // Виклик системної функції

.section .data
    filename: .asciz "test.txt"  // Ім'я файлу для зміни прав доступу
